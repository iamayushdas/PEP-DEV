let str = 'pepcoder'


// split

let partOfString = str.split('p')
// split function will split a string according to a passed parameter
// and will return the left and the right part in an array
console.log(partOfString);




// join

let joinStr  = partOfString.join('p')
// join func will take an  array and it will fill the empty spaces
// with the passed parametet and returns a complete string

console.log(joinStr);